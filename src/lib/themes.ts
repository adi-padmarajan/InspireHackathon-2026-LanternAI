// Theme System for Lantern - Award-Winning Cinematic Collection
// 20 Stunning, Immersive Themes for an Unforgettable Experience

export type ThemeCategory = 'signature' | 'cinematic' | 'atmospheric' | 'artistic' | 'luxury' | 'futuristic' | 'wonder';
export type ParticleVariant = 'fireflies' | 'orbs' | 'bubbles' | 'stars' | 'snow' | 'leaves' | 'sparks' | 'aurora' | 'rain' | 'embers' | 'crystals' | 'glitch' | 'nebula' | 'lightning';
export type SpringPreset = 'gentle' | 'bouncy' | 'snappy' | 'dreamy' | 'dramatic' | 'ethereal';
export type AnimationIntensity = 'none' | 'subtle' | 'normal' | 'energetic' | 'cinematic';
export type BackgroundStyle = 'particles' | 'orbs' | 'minimal' | 'dynamic' | 'immersive';

export interface ThemeColors {
  background: string;
  foreground: string;
  card: string;
  cardForeground: string;
  primary: string;
  primaryForeground: string;
  secondary: string;
  secondaryForeground: string;
  accent: string;
  accentForeground: string;
  muted: string;
  mutedForeground: string;
  border: string;
  input: string;
  ring: string;
  // Theme-specific
  glowColor: string;
  glowColorSoft: string;
  particleColor: string;
  orbColor1: string;
  orbColor2: string;
}

export interface ThemeParticleConfig {
  variant: ParticleVariant;
  count: number;
  baseColor: string;
  secondaryColor?: string;
  tertiaryColor?: string;
  speed: 'slow' | 'medium' | 'fast';
  size: 'small' | 'medium' | 'large';
}

export interface ThemeAnimationConfig {
  springPreset: SpringPreset;
  glowIntensity: number;
  floatAmplitude: number;
}

export interface Theme {
  id: string;
  name: string;
  category: ThemeCategory;
  description: string;
  tagline: string;
  icon: string;
  colors: {
    light: ThemeColors;
    dark: ThemeColors;
  };
  particles: ThemeParticleConfig;
  animation: ThemeAnimationConfig;
  preview: {
    gradient: string;
    accentColors: string[];
  };
}

export interface ThemeSettings {
  themeId: string;
  colorMode: 'light' | 'dark' | 'system';
  animationIntensity: AnimationIntensity;
  backgroundStyle: BackgroundStyle;
  customAccentColor: string | null;
}

